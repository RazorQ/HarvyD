#include <iostream>
using namespace std;

int main()
{
    double n1, n2;

    cout << "Введите первое число: ";
    cin >> n1;
    cout << "Введите второе число: ";
    cin >> n2;

    cout << n1 << " + " << n2 << " = " << n1 + n2 << endl;
    cout << n1 << " - " << n2 << " = " << n1 - n2 << endl;
    cout << n1 << " * " << n2 << " = " << n1 * n2 << endl;
    cout << n1 << " / " << n2 << " = " << n1 / n2 << endl;
}

